<h1>CRUD Home Page</h1>
<hr>
<p>Please choose database on which you want to perform CRUD operations from the left menu.</p>
<span>Description of menu options:</span>
<ul>
    <li><b>E: Mongo</b> - Mongo DB Collection `products` where raw data from html lands</li>
    <li><b>T: Mongo</b> - Mongo DB Collection `aggregated` where aggregated selected data lands during transform</li>
    <li><b>T: SQL</b> - SQL Table with structured data with converted data-types after full transform</li>
    <li><b>L: SQL</b> - SQL Table which represents target Data Warehouse</li>
</ul>